COMMENT ON COLUMN 	PUBLIC.TB_DESPESA_VARIAVEL.ID_RESPONSAVEL_PAGAMENTO 	IS 'CHAVE ESTRANGEIRA DA TABELA TB_RESPONSAVEL_PAGAMENTO';
